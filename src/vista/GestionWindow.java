    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        try {
            DefaultListModel dfm = new DefaultListModel();
            ListCat lc = c.ActualizarCategorias();
            ListaUsu lu = c.ActualizarUsuario();
            cbEmpleado.removeAllItems();
            
            for (var e : lu.usuarios){
                cbEmpleado.addItem(e.getNombre());
            }
            
            c.ActualizarProductos();
            for (var ca : lc.categorias){
                dfm.addElement(ca.nom);
            }
            ListCategorias.setModel(dfm);
        } catch (SQLException ex) {
            Logger.getLogger(GestionWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formComponentShown


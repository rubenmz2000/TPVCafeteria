    private void bInsertarProdCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInsertarProdCActionPerformed
        try{
            if (listProd.getSelectedIndex() != -1){
                String nombre = listProd.getSelectedValue();
                ListaProd lp = c.productos;
                ProductosComanda pc = new ProductosComanda();
                for (var p : lp.lp){
                    if (p.getNombre().equals(nombre)){
                        pc = c.InsertarEnComanda(p);
                    }
                }
                RellenarComanda(pc);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_bInsertarProdCActionPerformed


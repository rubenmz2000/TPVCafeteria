    private void BelimProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BelimProdActionPerformed
        try {    
            int i;
            if ((i = tProd.getSelectedRow()) != -1){
            
                Producto p = new Producto(tProd.getValueAt(i, 0).toString(), tProd.getValueAt(i, 1).toString(), tProd.getValueAt(i, 2).toString(), Double.parseDouble(tProd.getValueAt(i, 4).toString()), tProd.getValueAt(i, 3).toString());
                ListaProd lp = c.BorrarProducto(p);
                DefaultTableModel dfm = new DefaultTableModel();
                
                dfm.addColumn("id");
                dfm.addColumn("nombre");
                dfm.addColumn("categoria");
                dfm.addColumn("info");
                dfm.addColumn("precio");
                for (var ca : lp.lp){
                    dfm.addRow(new Object[] {ca.getId(), ca.getNombre(), ca.getCategoria(), ca.getInfo(), ca.getPrecio()});
                }
                tProd.setModel(dfm);
            
            }
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_BelimProdActionPerformed


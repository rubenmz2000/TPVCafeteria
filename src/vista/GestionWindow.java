    private void BelimCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BelimCatActionPerformed
        try {
            Categoria cat = new Categoria();
            cat.nom = listaGCat.getSelectedValue();
            DefaultListModel dfm = new DefaultListModel();
            ListCat lc = c.EliminarCategoria(cat);
            for (var ca : lc.categorias){
                dfm.addElement(ca.nom);
                CbCategorias.addItem(ca.nom);
            }

            ListCategorias.setModel(dfm);
            listaGCat.setModel(dfm);
        } catch (SQLException ex) {
            Logger.getLogger(GestionWindow.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_BelimCatActionPerformed

    private void BinsCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BinsCatActionPerformed
        Categoria cat = new Categoria(TxtCodCat.getText(), TxtNomCat.getText());
        try {
            ListCat lc = c.InsertarCategoria(cat);
            DefaultListModel dfm = new DefaultListModel();
            CbCategorias.removeAllItems();
            for (var ca : lc.categorias){
                dfm.addElement(ca.getNom());

                CbCategorias.addItem(ca.nom);
            }

            ListCategorias.setModel(dfm);
            listaGCat.setModel(dfm);
        } catch (SQLException ex) {
            Logger.getLogger(GestionWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BinsCatActionPerformed

